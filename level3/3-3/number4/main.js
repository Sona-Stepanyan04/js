// Given an array:
//
//     [
//         [
//             [11, 12, 13],
//             [14, 15, 16],
//             [17, 17, 19],
//         ],
//         [
//             [21, 22, 23],
//             [24, 25, 26],
//             [27, 27, 29],
//         ],
//         [
//             [31, 32, 33],
//             [34, 35, 36],
//             [37, 37, 39],
//         ],
//     ]
// Find the sum of the elements of this array.

let arr = [
    [
        [11, 12, 13],
        [14, 15, 16],
        [17, 17, 19],
    ],
    [
        [21, 22, 23],
        [24, 25, 26],
        [27, 27, 29],
    ],
    [
        [31, 32, 33],
        [34, 35, 36],
        [37, 37, 39],
    ],
];
let sum = arr.flat(Infinity).reduce((a, b) => a + b, 0);
console.log(sum);